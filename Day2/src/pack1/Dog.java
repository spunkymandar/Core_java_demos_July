package pack1;

public class Dog extends Animal{
	
	//overridden method
	public void talk() {
		System.out.println("Dog barks");
	}
	//overridden method
	public void eat() {
		System.out.println("Dog eats nonveg");
	}
	
	public void read() {
		System.out.println("Times of India");
	}
	
	public void train() {
		System.out.println("Dogs can be trained");
	}

}
